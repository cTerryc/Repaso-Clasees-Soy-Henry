//Sentencia While para repetir en bucle mientras que sea verdad
//Ejemplo;
var i = 0;
while ("tiene q ser verdad") {
    "aqui va el codigo"
    //se tiene q añadir al final un i++ antes de cerrar el codigo
    i++
}

//Do While se ejecuta almenos una vez
//Ejemplo:
var i = 0;
do {
    "aqui va el codigo"
    //se tiene q añadir al final un i++ antes de cerrar el codigo
    i++
} while ("tiene q ser verdad");